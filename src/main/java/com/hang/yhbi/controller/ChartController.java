import cn.hutool.core.io.FileUtil;
import com.baomidou.mybatisplus.extension.plugins.pagination.Page;
import com.hang.yhbi.annotation.AuthCheck;
import com.hang.yhbi.common.BaseResponse;
import com.hang.yhbi.common.DeleteRequest;
import com.hang.yhbi.common.ErrorCode;
import com.hang.yhbi.common.ResultUtils;
import com.hang.yhbi.constant.UserConstant;
import com.hang.yhbi.exception.BusinessException;
import com.hang.yhbi.exception.ThrowUtils;
import com.hang.yhbi.manager.AIManager;
import com.hang.yhbi.manager.RedisLimiterManager;
import com.hang.yhbi.model.dto.chart.*;
import com.hang.yhbi.model.entity.Chart;
import com.hang.yhbi.model.entity.User;
import com.hang.yhbi.model.enums.AIEnum;
import com.hang.yhbi.model.vo.BIResponse;
import com.hang.yhbi.service.ChartService;
import com.hang.yhbi.service.UserService;
import com.hang.yhbi.utils.ExcelUtils;
import lombok.extern.slf4j.Slf4j;
import org.apache.commons.lang3.StringUtils;
import org.springframework.beans.BeanUtils;
